package com.if3a.paimonopedia.models;

public class Characters {

    private String name, title, vision, weapon, nation, affiliation, constellation, birthday, description, skillTalents, passiveTalents;
    private Integer rarity;

    public String getName() {
        return name;
    }

    public String getTitle() {
        return title;
    }

    public String getVision() {
        return vision;
    }

    public String getWeapon() {
        return weapon;
    }

    public String getNation() {
        return nation;
    }

    public String getAffiliation() {
        return affiliation;
    }

    public String getConstellation() {
        return constellation;
    }

    public String getBirthday() {
        return birthday;
    }

    public String getDescription() {
        return description;
    }

    public String getSkillTalents() {
        return skillTalents;
    }

    public String getPassiveTalents() {
        return passiveTalents;
    }

    public Integer getRarity() {
        return rarity;
    }
}
