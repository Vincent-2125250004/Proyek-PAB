package com.if3a.paimonopedia.models;

public class ArtifactSet {
    public String name, twoPieceBonus, fourPieceBonus;
    public int maxRarity;


    public String getName() {
        return name;
    }

    public String getTwoPieceBonus() {
        return twoPieceBonus;
    }

    public String getFourPieceBonus() {
        return fourPieceBonus;
    }

    public int getMaxRarity() {
        return maxRarity;
    }
}
