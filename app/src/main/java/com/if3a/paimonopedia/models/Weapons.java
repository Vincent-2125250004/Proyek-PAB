package com.if3a.paimonopedia.models;

public class Weapons {

    private String name, type, subStat, passiveName, passiveDesc, location, ascensionMaterial;
    private Integer rarity, baseAttack;

    public String getName() {
        return name;
    }

    public String getType() {
        return type;
    }

    public String getSubStat() {
        return subStat;
    }

    public String getPassiveName() {
        return passiveName;
    }

    public String getPassiveDesc() {
        return passiveDesc;
    }

    public String getLocation() {
        return location;
    }

    public String getAscensionMaterial() {
        return ascensionMaterial;
    }

    public Integer getRarity() {
        return rarity;
    }

    public Integer getBaseAttack() {
        return baseAttack;
    }
}
